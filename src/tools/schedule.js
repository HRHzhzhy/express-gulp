import schedule from 'node-schedule'
/**
 * *    *    *    *    *    *
┬    ┬    ┬    ┬    ┬    ┬
│    │    │    │    │    |
│    │    │    │    │    └ day of week (0 - 7) (0 or 7 is Sun)
│    │    │    │    └───── month (1 - 12)
│    │    │    └────────── day of month (1 - 31)
│    │    └─────────────── hour (0 - 23)
│    └──────────────────── minute (0 - 59)
└───────────────────────── second (0 - 59, OPTIONAL)
 * @param {String} rule
 * @param {Function} cb
 */
export const setSchedule = (rule, cb) => {
  return schedule.scheduleJob(rule, cb)
}
export default exports
